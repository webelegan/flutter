double div(double a, double b) {
   return a / b;
}

void main() {
  // memanggil fungsi dengan nilai argumen 15.0 dan 3.0
  double result = div(15.0, 3.0);
  
  // menampilkan hasil fungsi ke layar
  print(result);
}
