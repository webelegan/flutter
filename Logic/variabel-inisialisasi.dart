void main() {
   // inisialisasi variabel
   int a = 1;

   // menampilkan nilai awal
   print('Nilai awal a: ' + a.toString());

   // mengubah nilai a
   a = 13;

   // menampilkan nilai setelah diubah
   print('Nilai a setelah diubah: ' + a.toString());
}
